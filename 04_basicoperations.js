// ****************** Operations ******************
let value = 11;
let negvalue = -value;                 // Unary negation
console.log(negvalue);                 // Output: -11

// Mathematical operations
console.log(2 + 2);    // 4
console.log(2 - 2);    // 0
console.log(2 * 2);    // 4
console.log(2 ** 2);   // 4 (2 raised to power 2)

// String addition (concatenation)
let str1 = "hi";
let str2 = "yuvraj";
let str3 = str1 + str2;
console.log(str3);     // Output: "hiyuvraj"

// Number + string examples (Type coercion in JavaScript)
console.log("1" + 2);       // "12"  → number 2 is converted to string
console.log(2 + "3");       // "23"  → number 2 is converted to string
console.log("2" + "3");     // "23"  → both are strings
console.log("1" + "2" + "2"); // "122" → all are strings
console.log(1 + 2 + "2");   // "32" → (1+2=3) + "2" = "32"




